#include<iostream>
using namespace std;

int main(){
    int a = 4, b = 4;

    // Arthimetik operetor
    cout<<"the sum is a and b :"<<a +b<<endl;
    cout<<"the - is a and b :"<<a -b<<endl;
    cout<<"the * is a and b :"<<a *b<<endl;
    cout<<"the % is a and b :"<<a %b<<endl;
    cout<<"the / is a and b :"<<a / b<<endl;


    // compaison operetor
    cout<<"a==b :" <<(a==b)<<endl;
    cout<<"a!=b :" <<(a!=b)<<endl;
    cout<<"a<=b :" <<(a<=b)<<endl;
    cout<<"a<=b :" <<(a>=b)<<endl;
    cout<<"a<=b :" <<(a>b)<<endl;
    cout<<"a<=b :" <<(a<b)<<endl;
    return 0;

}